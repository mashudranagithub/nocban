                            <p>Bangladesh Olympic Association, also known as National Olympic Committee of Bangladesh is the national constituent of the worldwide Olympic movement. Subject to the controls of the International Olympic Committee, BOA is responsible for organizing its people's participation in the Olympic Games. BOA was organized in 1976 and got its recognition in 1980. The organization works for development and promotion of the Olympic Movement in the country. At the same time, it provides support to the Government for the development of sport in Bangladesh, by maintaining full autonomy in line with the IOC Charter. 
</p>
                            <p>In Bangladesh BOA plays a major role in developing and nurturing sportsmanship, sporting events and activities related to the development of sports. BOA works closely with other government and non government organization while retaining its autonomous identity. BOA maintains cooperative relationship with governmental and non-governmental organization in order to best benefit the sports of Bangladesh. In short BOA strives to improve the quality of sports in Bangladesh while spread the values Olympism. It has so far affiliated a total of 27 National Federations (NF) and National Associations (NA) that includes both Olympic and non-Olympic sports. Bangladesh is the breeding ground of amateur sporting clubs. National Teams of various sport are selected from the inter club competition and from the National Meet of the respective sport.</p>

                            <h4>OUR SPORTS</h4>

                            <p>Tradition, culture and passion weaved the history of Bangladesh sports. Football was the mainstream sport before the independence of Bangladesh. In reality, most of the sporting events used to be held in the West Pakistan. </p>
                            <p>Before the independence, a swimmer of this hot reverine region, Brojen Das, created world record in 1958 by crossing the ice cold English Channel. This Bengali swimmer of the erstwhile East Pakistan crossed the channel more than once in record time and introduced the Bengali people to the world of sport. During the liberation war, the Swadhin (independent) Bangla Football Team helped popularize the cause of our freedom movement. Another legendary swimmer created record by being afloat for 72 hours at a stretch in a Calcutta pond to draw attention of the entire world to the cause of Bangladesh's independence. Through sports the message of the Liberation War of Bangladesh quickly drew attention of the international community.</p>

                            <p>Just after independence, under patronization of Bangladesh's first Prime Minister Bangabandhu Sheikh Muzibur Rahman, Bangladesh Government established Football, Hockey, Gymnastics, Athletics, Swimming, Boxing Federation and Bangladesh Women Sports Control Board in 1972. 
</p>

                            <p>Boxer Abdul Halim earned the first International success winning bronze in Asian Youth Boxing Championship in 1977 followed by Rani Hamid winning title of British Women Chess Championship in 1983. Though not an Olympic discipline, Cricket is now the most popular sport in Bangladesh. Football, from the very beginning, caught the imagination of Bangladeshi people. Presently, sports like Hockey, Shooting, Athletics, Volleyball, Handball, Kabaddi, Basketball, Badminton, Table Tennis have earned significant degrees of popularity. Of late Archery, Golf, Judo, Karate, Taekwondo, Roller Skating, Fencing, Equestrian and Squash have added a new dimension to our sports. Surfing also makes its identity in our sporting scenario.</p>

                                                                    <p>National Sports Council (NSC) and the Ministry of Youth and Sports are tasked to develop sports, establishing, renovating and maintaining the physical infrastructures of games and sports in Bangladesh.</p>

